def SelectColor(ColorName):
    if ColorName == 'Yellow':
        return [255,255,0]
    if ColorName == 'Blue':
        return [0,0,255]
    if ColorName == 'Red':
        return [255,0,0]
    if ColorName == 'Green':
        return [0,255,0]
    if ColorName == 'White':
        return [255,255,255]
    if ColorName == 'Purple':
        return [255,0,255]
    if ColorName == 'Light Blue':
        return [0,255,255]
    if ColorName == 'Gray1':
        return [100,100,100]
    if ColorName == 'Gray2':
        return [200,200,200]
    if ColorName == 'Gray3':
        return [50,50,50]
    if ColorName == 'Black':
        return [0,0,0]
    if ColorName == 'Orange':
        return [227,113,36]